package XXLChess.utils;

public class Color {
    private final int R;
    private final int G;
    private final int B;


    public Color(int r, int g, int b) {
        R = r;
        G = g;
        B = b;
    }

    public int getR() {
        return R;
    }

    public int getG() {
        return G;
    }

    public int getB() {
        return B;
    }
}
